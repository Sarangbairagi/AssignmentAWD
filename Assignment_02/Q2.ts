function Summation(iBrr : number[]) : number 
{
    var iCnt : number = 0;
    var iSum : number = 0;

    for(iCnt = 0;iCnt < iBrr.length; iCnt++)
    {
        iSum = iSum + iBrr[iCnt];
    }

    return iSum;

} 

