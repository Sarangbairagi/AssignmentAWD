//  Write a program to accept array of numbers and print that Summation each numbers array
///////////////////////////////////////////////////////////////////////////////////////////////

var iArr : number[] = [23,6,7,4,5,7];
var iRet : number = 0;

iRet = Summation(iArr);

console.log("Addititon is "+iRet);


